/// Object-Oriented Programming in Dart

/*
THEORY:

1) Dart is an Object-Oriented language.
2) Object Orientation considers a program as a collection of objects that communicate with each other via mechanism called methods.
*/

class MyClass {
  void display() {
    print("Hello, I am Anurag.");
  }
}

void main() {
  MyClass obj = new MyClass();
  obj.display();
}
